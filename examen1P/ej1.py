#escriba un programa que pida al usuario dos palabras, y que indique cuale de ellas es mas larga y por cuantas letras lo es

palabra = input("Primera palabra: ")
word = input("Segunda palabra: ")

numlet = len(palabra)
numelet = len(word)
if numlet > numelet:
    print(f"La primera palabra es más larga que la segunda por {numlet - numelet} letras.")
elif numlet < numelet:
    print(f"La segunda palabra es más larga que la primera por {numelet - numlet} letras.")
else:
    print("Las dos palabras tienen la misma longitud.")
